print("HTTP = protocol for requests and responses")
print("Methods: GET (read), POST (create), PUT/PATCH (update), DELETE (remove)")
print("Status codes: 200 OK, 201 Created, 400 Bad Request, 404 Not Found, 500 Server Error")
print("URL parts: https://api.example.com:443/users?id=1")
print("Headers: Content-Type, Authorization, Accept")
print("Body: often JSON for APIs")
